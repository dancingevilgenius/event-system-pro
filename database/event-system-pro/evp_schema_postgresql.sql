-- Table: competitor_type_lu
CREATE TABLE IF NOT EXISTS competitor_type_lu (
  competitor_type_id INTEGER NOT NULL,
  description varchar(32) NOT NULL,
  created_date TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_by varchar(128) NOT NULL DEFAULT 'c-agent',
  modified_date TIMESTAMPTZ NULL DEFAULT NULL,
  modified_by varchar(128) NULL DEFAULT NULL,
PRIMARY KEY (competitor_type_id)
);

-- Table: country_lu
CREATE TABLE IF NOT EXISTS country_lu (
  country_id INTEGER GENERATED BY DEFAULT AS IDENTITY,
  iso2 char(2) DEFAULT NULL,
  short_name varchar(80) NOT NULL DEFAULT '',
  long_name varchar(80) NOT NULL DEFAULT '',
  iso3 varchar(3) DEFAULT NULL,
  numcode varchar(6) DEFAULT NULL,
  un_member varchar(12) DEFAULT NULL,
  calling_code varchar(8) DEFAULT NULL,
  cctld varchar(5) DEFAULT NULL,
  active BOOLEAN DEFAULT TRUE,
  created_date TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_by varchar(128) NOT NULL DEFAULT 'c-agent',
  modified_date TIMESTAMPTZ NULL DEFAULT NULL,
  modified_by varchar(128) NULL DEFAULT NULL,
PRIMARY KEY (country_id)
);
CREATE INDEX IF NOT EXISTS iso3 ON country_lu (iso3);

-- Table: us_state_lu
