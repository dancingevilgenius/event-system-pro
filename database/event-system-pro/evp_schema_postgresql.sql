-- Table: contest
CREATE TABLE IF NOT EXISTS contest (
  contest_id bigint NOT NULL,
  event_id bigint DEFAULT NULL,
  competitors_json JSONB NOT NULL,
  results_json JSONB NOT NULL,
  age_division_id INTEGER DEFAULT NULL,
  created_by varchar(31) NOT NULL,
  created_date date NOT NULL,
  current_heat_id INTEGER DEFAULT NULL,
  current_heat_index INTEGER DEFAULT NULL,
  event_type_code varchar(32) NOT NULL,
  media_links_json varchar(255) DEFAULT NULL,
  is_active INTEGER NOT NULL,
  is_cancelled INTEGER DEFAULT NULL,
  staff_json varchar(1000) DEFAULT NULL,
  location_json varchar(1000) DEFAULT NULL,
  male_or_female varchar(1) DEFAULT NULL,
  modified_by varchar(31) NOT NULL,
  modified_date date NOT NULL,
  number_of_heats INTEGER DEFAULT NULL,
  original_end_time date DEFAULT NULL,
  original_start_time date NOT NULL,
  primary_contact_id INTEGER DEFAULT NULL,
  pushed_back_end_time date DEFAULT NULL,
  pushed_back_start_time date DEFAULT NULL,
PRIMARY KEY (contest_id),
CONSTRAINT contest_ibfk_1 FOREIGN KEY (event_id) REFERENCES "event" (event_id)
);
CREATE INDEX IF NOT EXISTS event_id ON contest (event_id);

-- Table: judging_panel
-- judges_json shape: [{"name": "...", "judge_id": <number|string>}]
CREATE TABLE IF NOT EXISTS judging_panel (
  judging_panel_id INTEGER GENERATED BY DEFAULT AS IDENTITY,
  event_id bigint NOT NULL,
  contest_id bigint NOT NULL,
  judges_json JSONB NOT NULL DEFAULT '[]'::jsonb,
  created_by varchar(128) NOT NULL,
  created_date TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  modified_by varchar(128) NULL DEFAULT NULL,
  modified_date TIMESTAMPTZ NULL DEFAULT NULL,
PRIMARY KEY (judging_panel_id),
CONSTRAINT judging_panel_fk_event FOREIGN KEY (event_id) REFERENCES "event" (event_id) ON DELETE RESTRICT,
CONSTRAINT judging_panel_fk_contest FOREIGN KEY (contest_id) REFERENCES contest (contest_id) ON DELETE RESTRICT
);
CREATE INDEX IF NOT EXISTS judging_panel_fk_event ON judging_panel (event_id);
CREATE INDEX IF NOT EXISTS judging_panel_fk_contest ON judging_panel (contest_id);

-- Table: competitor
CREATE TABLE IF NOT EXISTS competitor (
  competitor_id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  contest_id bigint NOT NULL,
  event_id bigint NOT NULL,
  user_id bigint DEFAULT NULL,
  first_name varchar(32) DEFAULT NULL,
  last_name varchar(32) DEFAULT NULL,
  gear_json varchar(255) DEFAULT NULL,
  competitor_type_id INTEGER DEFAULT NULL,
  created_date TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_by varchar(128) NOT NULL DEFAULT 'c-agent',
  modified_date TIMESTAMPTZ NULL DEFAULT NULL,
  modified_by varchar(128) NULL DEFAULT NULL,
PRIMARY KEY (competitor_id),
CONSTRAINT competitor_fk_contest FOREIGN KEY (contest_id) REFERENCES contest (contest_id) ON DELETE RESTRICT,
CONSTRAINT competitor_fk_event FOREIGN KEY (event_id) REFERENCES "event" (event_id) ON DELETE RESTRICT,
CONSTRAINT competitor_fk_user FOREIGN KEY (user_id) REFERENCES "user" (user_id) ON DELETE RESTRICT ON UPDATE RESTRICT
);
CREATE INDEX IF NOT EXISTS fk_competitor_type ON competitor (competitor_type_id);
CREATE INDEX IF NOT EXISTS competitor_fk_contest ON competitor (contest_id);
CREATE INDEX IF NOT EXISTS competitor_fk_event ON competitor (event_id);
CREATE INDEX IF NOT EXISTS fk_competitor_user_id ON competitor (user_id);

-- Table: contest_heat
CREATE TABLE IF NOT EXISTS contest_heat (
  heat_id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  event_id bigint NOT NULL,
  contest_id bigint NOT NULL,
  is_currently_being_run INTEGER DEFAULT NULL,
  original_start_time TIMESTAMPTZ DEFAULT NULL,
  original_end_time TIMESTAMPTZ DEFAULT NULL,
  pushed_back_start_time TIMESTAMPTZ DEFAULT NULL,
  pushed_back_end_time TIMESTAMPTZ DEFAULT NULL,
  heat_index INTEGER DEFAULT NULL,
  next_heat_id INTEGER DEFAULT NULL,
  judging_panel_id INTEGER DEFAULT NULL,
  all_judges_finished_scoring INTEGER DEFAULT NULL,
  num_judges_wanting_more INTEGER DEFAULT NULL,
  has_mixed_divisions INTEGER DEFAULT NULL,
  created_by varchar(128) NOT NULL,
  created_date varchar(128) NOT NULL,
  modified_by varchar(128) NULL DEFAULT NULL,
  modified_date varchar(128) NOT NULL,
PRIMARY KEY (heat_id),
CONSTRAINT heat_fk_contest FOREIGN KEY (contest_id) REFERENCES contest (contest_id),
CONSTRAINT heat_fk_event FOREIGN KEY (event_id) REFERENCES "event" (event_id)
);
CREATE INDEX IF NOT EXISTS heat_fk_contest ON contest_heat (contest_id);
CREATE INDEX IF NOT EXISTS heat_fk_event ON contest_heat (event_id);

-- Table: fight_event_staff
