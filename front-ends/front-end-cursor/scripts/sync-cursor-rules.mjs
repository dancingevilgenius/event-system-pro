import { cpSync, existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageRoot = join(scriptDir, '..');
const repoRulesDir = join(packageRoot, '../../.cursor/rules');
const destDir = join(packageRoot, 'src/data/cursor-rules');
const generatedFile = join(packageRoot, 'src/lib/cursorRules.generated.ts');

function resolveExistingDir(candidates) {
  for (const candidate of candidates) {
    if (existsSync(candidate)) {
      return candidate;
    }
  }

  return null;
}

const sourceDir = resolveExistingDir([repoRulesDir, destDir]);

if (!sourceDir) {
  console.error('sync-cursor-rules: no Cursor rules directory found.');
  process.exit(1);
}

mkdirSync(destDir, { recursive: true });

if (resolve(sourceDir) !== resolve(destDir)) {
  const sourceFiles = readdirSync(sourceDir).filter((name) => name.endsWith('.mdc'));

  if (sourceFiles.length === 0) {
    console.error(`sync-cursor-rules: no .mdc files found in ${sourceDir}`);
    process.exit(1);
  }

  for (const fileName of sourceFiles) {
    cpSync(join(sourceDir, fileName), join(destDir, fileName));
  }
}

const ruleFiles = readdirSync(destDir)
  .filter((name) => name.endsWith('.mdc'))
  .sort((a, b) => a.localeCompare(b));

if (ruleFiles.length === 0) {
  console.error(`sync-cursor-rules: no .mdc files found in ${destDir}`);
  process.exit(1);
}

const entries = ruleFiles.map((fileName) => {
  const id = fileName.replace(/\.mdc$/, '');
  const raw = readFileSync(join(destDir, fileName), 'utf8');
  return `  ${JSON.stringify(id)}: ${JSON.stringify(raw)},`;
});

writeFileSync(
  generatedFile,
  `// Generated by scripts/sync-cursor-rules.mjs — do not edit.\n`
    + `export const CURSOR_RULE_RAW: Record<string, string> = {\n`
    + `${entries.join('\n')}\n`
    + `};\n`,
  'utf8',
);

console.log(
  `sync-cursor-rules: synced ${ruleFiles.length} rule file(s) and wrote src/lib/cursorRules.generated.ts`,
);
